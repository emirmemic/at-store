import { AuthUserNavButton } from '@/components/custom/auth/auth-user-button';
import { GoogleSignInButton } from '@/components/custom/auth/google-sign-in-button';
import { Button } from '@/components/ui/button';
import { getUser } from '@/lib/hooks/services';
import type { Metadata } from 'next';
import Link from 'next/link';
import { SF_Pro, SF_Pro_Text } from './fonts/fonts';
import './globals.css';

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const user = await getUser();
  return (
    <html lang="en" className={`${SF_Pro.variable} ${SF_Pro_Text.variable}`}>
      <body className={`${SF_Pro.variable}`}>
        <div className="flex items-center justify-between p-4">
          <Link href={'/'}>Home page</Link>
          {user ? (
            <AuthUserNavButton user={user} />
          ) : (
            <>
              <Link href={'/login'} className="">
                login
              </Link>
              <Button asChild>
                <GoogleSignInButton />
              </Button>
              {/* <Button asChild>
                <GithubSignInButton />
              </Button> */}
            </>
          )}
        </div>
        {children}
      </body>
    </html>
  );
}
